package accessSpecifiers.com;


 public class protected_1 {
	 protected int price;
	 protected int lenght, width;
	 protected String name;
	
	 protected protected_1(int price, int lenght, int width, String name) {
	
		this.price = price;
		this.lenght = lenght;
		this.width = width;
		this.name = name;
	}
	
	protected int getPrice() {
		return price;
	}
	
	protected int getLenght() {
		return lenght;
	}
	
	protected int getWidth() {
		return width;
	}
	
	protected String getName() {
		return name;
	}


	
	 		
}
 





